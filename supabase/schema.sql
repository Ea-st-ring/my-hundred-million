-- 4) 적금 정보
create table if not exists public.installment_savings (
	id bigint generated by default as identity primary key,
	name text not null,
	monthly_amount bigint not null default 0,
	saved_amount bigint not null default 0,
	is_recurring boolean not null default false,
	cadence text check (cadence in ('WEEKLY', 'MONTHLY')),
	run_day smallint check (run_day between 1 and 31),
	apply_mode text not null default 'TODAY' check (apply_mode in ('TODAY', 'NEXT_CYCLE')),
	recurring_started_at timestamptz,
	start_date date not null,
	maturity_date date,
	benefit_type text not null default 'INTEREST_RATE' check (benefit_type in ('INTEREST_RATE', 'MATURITY_AMOUNT')),
	benefit_value numeric(18, 3) not null default 0,
	created_at timestamptz not null default timezone('utc', now()),
	updated_at timestamptz not null default timezone('utc', now())
);

alter table public.installment_savings
add column if not exists benefit_type text not null default 'INTEREST_RATE'
check (benefit_type in ('INTEREST_RATE', 'MATURITY_AMOUNT'));

alter table public.installment_savings
add column if not exists benefit_value numeric(18, 3) not null default 0;

alter table public.installment_savings
add column if not exists saved_amount bigint not null default 0;

alter table public.installment_savings
add column if not exists is_recurring boolean not null default false;

alter table public.installment_savings
add column if not exists cadence text check (cadence in ('WEEKLY', 'MONTHLY'));

alter table public.installment_savings
add column if not exists run_day smallint check (run_day between 1 and 31);

alter table public.installment_savings
add column if not exists apply_mode text not null default 'TODAY'
check (apply_mode in ('TODAY', 'NEXT_CYCLE'));

alter table public.installment_savings
add column if not exists recurring_started_at timestamptz;

alter table public.installment_savings
alter column maturity_date drop not null;

update public.installment_savings
set is_recurring = monthly_amount > 0
where is_recurring = false
	and monthly_amount > 0;

update public.installment_savings
set cadence = 'MONTHLY'
where is_recurring = true
	and cadence is null;

update public.installment_savings
set run_day = 1
where is_recurring = true
	and run_day is null;

update public.installment_savings
set recurring_started_at = created_at
where is_recurring = true
	and recurring_started_at is null;

-- 4-1) 적금 정기 납입 자동 반영 로그
create table if not exists public.installment_contribution_logs (
	id bigint generated by default as identity primary key,
	installment_id bigint not null references public.installment_savings(id) on delete cascade,
	run_date date not null,
	amount bigint not null,
	created_at timestamptz not null default timezone('utc', now()),
	unique (installment_id, run_date)
);

-- updated_at 자동 갱신
create or replace function public.touch_updated_at()
returns trigger
language plpgsql
as $$
begin
	new.updated_at = timezone('utc', now());
	return new;
end;
$$;

drop trigger if exists trg_finance_overview_updated_at on public.finance_overview;
create trigger trg_finance_overview_updated_at
before update on public.finance_overview
for each row
execute function public.touch_updated_at();

drop trigger if exists trg_stock_holdings_updated_at on public.stock_holdings;
