-- 1) 월급/지출 (단일 row)
create table if not exists public.finance_overview (
	id integer primary key default 1 check (id = 1),
	salary bigint not null default 0,
	fixed_expense bigint not null default 0,
	variable_expense bigint not null default 0,
	updated_at timestamptz not null default timezone('utc', now())
);

insert into public.finance_overview (id)
values (1)
on conflict (id) do nothing;

-- 2) 주식 보유/모으기 상태
create table if not exists public.stock_holdings (
	id bigint generated by default as identity primary key,
	broker text not null check (broker in ('TOSS', 'SAMSUNG')),
	market text not null check (market in ('KR', 'US')),
	symbol text not null,
	name text not null,
	quote_symbol text not null,
	quantity numeric(18, 6) not null default 0,
	average_price numeric(18, 4) not null default 0,
	is_accumulating boolean not null default false,
	accumulation_started_at timestamptz,
	cadence text check (cadence in ('WEEKLY', 'MONTHLY')),
	run_day smallint check (run_day between 1 and 31),
	accumulation_type text not null check (accumulation_type in ('AMOUNT', 'SHARES')),
	accumulation_currency text not null default 'KRW' check (accumulation_currency in ('KRW', 'USD')),
	accumulation_value numeric(18, 6) not null default 0,
	created_at timestamptz not null default timezone('utc', now()),
	updated_at timestamptz not null default timezone('utc', now())
);

alter table public.stock_holdings
add column if not exists is_accumulating boolean not null default false;

alter table public.stock_holdings
add column if not exists cadence text check (cadence in ('WEEKLY', 'MONTHLY'));

alter table public.stock_holdings
add column if not exists run_day smallint check (run_day between 1 and 31);

alter table public.stock_holdings
add column if not exists accumulation_started_at timestamptz;

do $$
begin
	if not exists (
		select 1
		from information_schema.columns
		where table_schema = 'public'
			and table_name = 'stock_holdings'
			and column_name = 'accumulation_currency'
	) then
		alter table public.stock_holdings
		add column accumulation_currency text not null default 'KRW'
		check (accumulation_currency in ('KRW', 'USD'));

		update public.stock_holdings
		set accumulation_currency = case
			when market = 'US' then 'USD'
			else 'KRW'
		end;
	end if;
end;
$$;

update public.stock_holdings
set accumulation_started_at = created_at
where is_accumulating = true
	and accumulation_started_at is null;

-- 2-1) 자동 반영 로그
create table if not exists public.stock_accumulation_logs (
	id bigint generated by default as identity primary key,
	holding_id bigint not null references public.stock_holdings(id) on delete cascade,
	run_date date not null,
	local_amount numeric(18, 6) not null,
	currency text not null check (currency in ('KRW', 'USD')),
	fx_rate numeric(18, 6),
	krw_amount bigint not null,
	created_at timestamptz not null default timezone('utc', now()),
	unique (holding_id, run_date)
);

